public void populaCmb(){
        ArrayList <Fornecedor> c = new Fornecedor().ConsultaTodos();
        forne.removeAllItems();
        forne.addItem("Selecione");
        for (int r=0; r<c.size();r++) {
            forne.addItem(c.get(r).getRazaosocial());
        }
    }
