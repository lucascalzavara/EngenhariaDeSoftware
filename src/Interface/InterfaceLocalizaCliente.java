    private void preencherTabela(ArrayList<Cliente> clis) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Raz達o Social", "CNPJ", "Nome Fantasia", "Rua", "Bairro", "Numero", "Cidade", "UF"});
        Object[] fila = new Object[modelo.getColumnCount()];
        for (int i = 0; i < clis.size(); i++) {
            fila[0] = clis.get(i).getRazaosocial();
            fila[1] = clis.get(i).getCnpj();
            fila[2] = clis.get(i).getNomefantasia();
            fila[3] = clis.get(i).getRua();
            fila[4] = clis.get(i).getBairro();
            fila[5] = clis.get(i).getNumero();
            fila[6] = clis.get(i).getCidade();
            fila[7] = clis.get(i).getUf();
            modelo.addRow(fila);
        }
        tabela.setModel(modelo);
    }
    
    private void consultarTodos() {
        Cliente c = new Cliente();
        clis = c.ConsultaTodos();
        if( clis.isEmpty()){
            JOptionPane.showMessageDialog(null, "banco vazio");      
        }
        preencherTabela(clis);
    }

