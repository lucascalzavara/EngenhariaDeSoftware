    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ("  .   .   /    -  ".equals(cnpj.getText()) && razao.getText().isEmpty()) {
            consultarTodos();
        }
        else if(razao.getText().isEmpty()){
            Cliente c = new Cliente (cnpj.getText());
            clis = c.ConsultaCnpj();
            if(clis.isEmpty()){
                JOptionPane.showMessageDialog(null, "N達o encontrado", "Mensagem", JOptionPane.ERROR_MESSAGE, null);
                cnpj.setText(null);
                return;
            }
            preencherTabela(clis);
            cnpj.setText(null);
            
        }else{
            Cliente c = new Cliente(razao.getText(), 1);
            clis = c.ConsultaRazao();
            if( clis.isEmpty()){
                JOptionPane.showMessageDialog(null, "N達o encontrado", "Mensagem", JOptionPane.ERROR_MESSAGE, null);
                return;
            }
            preencherTabela(clis);
            razao.setText(null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

